//Array.prototype.concat()
//O método concat() é utilizado para mesclar dois ou mais arrays.

//Exemplo1:
elogios1= ["linda", "perfeita", "maravilhosa"];
elogios2 = ["bonita", "encantadora"];
elogios3 = elogios1.concat(elogios2);

console.log(elogios3);

//Resultado esperado: ['linda', 'perfeita', 'maravilhosa', 'bonita', 'encantadora']

